#!/usr/bin/env node

// Fake OpenAI-compatible chat server for the `golden-toolset-flow` scenario.
//
// The worker agent uses a BYOK ModelKit whose baseUrl points here, so the REAL
// tool-calling loop (orchestrator.runWorkerWithFsTools) drives this server. Unlike
// REPOHELM_FAKE_MODELS (which only affects streaming spec generation), the worker's
// callLlmWithModelKit always hits a real endpoint — this is that endpoint.
//
// It scripts a deterministic sequence of tool calls that exercises issue #22 A–E:
//   write_todos (E) → search_files regex+glob (A) → read_file png (D)
//   → web_fetch /docs (B) → start_process "git status" + read_process (C)
// then writes src/generated-summary.md echoing the REAL tool results, so the
// scenario's assertions verify genuine tool behavior end-to-end.
//
// It also serves GET /docs as the web_fetch target (keeps egress local, no SSRF).

const http = require("node:http");

const PORT = Number(process.env.REPOHELM_FAKE_LLM_PORT || process.argv[2] || 4399);
const DOCS_BODY = "CONTRACT_VERSION=v2-toolset\nfindOffer(code) is the offer lookup contract.\n";
const DOCS_URL = `http://127.0.0.1:${PORT}/docs`;

let callId = 0;
function makeCall(name, args) {
  callId += 1;
  return { id: `call_${callId}`, type: "function", function: { name, arguments: JSON.stringify(args) } };
}

function reply(res, message) {
  const payload = {
    id: `chatcmpl-${Date.now()}`,
    object: "chat.completion",
    choices: [{ index: 0, finish_reason: message.tool_calls ? "tool_calls" : "stop", message }],
    usage: { prompt_tokens: 0, completion_tokens: 0 }
  };
  res.writeHead(200, { "Content-Type": "application/json" });
  res.end(JSON.stringify(payload));
}

// Classify the tool result messages already in the conversation.
function collect(messages) {
  const out = { search: null, image: null, web: null, todos: null, startHandle: null, readProc: null, readCount: 0, wrote: false };
  for (const m of messages) {
    if (m.role !== "tool" || typeof m.content !== "string") continue;
    let obj;
    try {
      obj = JSON.parse(m.content);
    } catch {
      continue;
    }
    if (Array.isArray(obj.matches)) {
      const hit = obj.matches[0];
      if (hit) out.search = `${hit.file}:${hit.line}`;
    } else if (obj.encoding === "base64") {
      out.image = { mediaType: obj.mediaType, bytes: obj.bytes };
    } else if (typeof obj.status === "number" && typeof obj.content === "string") {
      out.web = obj.content;
    } else if (Array.isArray(obj.todos)) {
      out.todos = obj.todos.length;
    } else if ("running" in obj) {
      out.readProc = { exitCode: obj.exitCode, running: obj.running, stdout: obj.stdout };
      out.readCount += 1;
    } else if (obj.path === "src/generated-summary.md" && obj.encoding === undefined) {
      out.wrote = true;
    } else if (obj.handle && obj.command) {
      out.startHandle = obj.handle;
    }
  }
  return out;
}

function countToolMessages(messages) {
  return messages.filter((m) => m.role === "tool").length;
}

// Poll budget: Turn A(1) + polls + write(1) + final(1) must stay within
// MAX_TOOL_LOOP_ITERATIONS (8). A freshly spawned process may still be running on
// the first read, so we poll a few times to try to catch a clean exit.
const MAX_PROCESS_POLLS = 4;

function buildSummary(c) {
  const contractVersion = (c.web && (c.web.match(/CONTRACT_VERSION=(\S+)/) || [])[1]) || "unknown";
  const procExit = c.readProc ? c.readProc.exitCode : "null";
  const procRunning = c.readProc ? c.readProc.running : "unknown";
  return [
    "# Toolset Flow Summary",
    "",
    "Generated by the BYOK worker using the built-in tool set (issue #22 A-E).",
    "",
    `search_hit=${c.search || "none"}`,
    `image_media_type=${c.image ? c.image.mediaType : "none"}`,
    `image_bytes=${c.image ? c.image.bytes : 0}`,
    `contract_version=${contractVersion}`,
    `todos=${c.todos == null ? 0 : c.todos}`,
    `process_started=${Boolean(c.startHandle)}`,
    `process_observed=${c.readProc != null}`,
    `process_exit=${procExit}`,
    `process_running=${procRunning}`,
    ""
  ].join("\n");
}

const server = http.createServer((req, res) => {
  if (req.method === "GET" && req.url && req.url.startsWith("/docs")) {
    res.writeHead(200, { "Content-Type": "text/plain" });
    res.end(DOCS_BODY);
    return;
  }
  if (req.method !== "POST") {
    res.writeHead(404).end();
    return;
  }
  let body = "";
  req.on("data", (chunk) => {
    body += chunk;
  });
  req.on("end", () => {
    let messages = [];
    try {
      messages = JSON.parse(body).messages || [];
    } catch {
      messages = [];
    }
    const n = countToolMessages(messages);
    const c = collect(messages);

    if (n === 0) {
      // Turn A: fan out across the new tools.
      reply(res, {
        role: "assistant",
        content: "Gathering inputs with the built-in tool set.",
        tool_calls: [
          makeCall("write_todos", {
            todos: [
              { content: "locate offer contract", status: "completed" },
              { content: "generate summary", status: "in_progress" }
            ]
          }),
          makeCall("search_files", { query: "find\\w+", regex: true, filePattern: "**/*.js" }),
          makeCall("read_file", { path: "assets/logo.png" }),
          makeCall("web_fetch", { url: DOCS_URL }),
          makeCall("start_process", { command: "git status" })
        ]
      });
      return;
    }

    if (!c.wrote) {
      const exited = c.readProc && c.readProc.running === false;
      // Poll the freshly spawned process until it exits, up to the budget.
      if (!exited && c.readCount < MAX_PROCESS_POLLS) {
        reply(res, {
          role: "assistant",
          content: "Polling the verification process output.",
          tool_calls: [makeCall("read_process", { handle: c.startHandle || "proc-1" })]
        });
        return;
      }
      // Write the summary echoing the real tool outputs.
      reply(res, {
        role: "assistant",
        content: "Writing src/generated-summary.md from the collected tool outputs.",
        tool_calls: [makeCall("write_file", { path: "src/generated-summary.md", content: buildSummary(c) })]
      });
      return;
    }

    // Finish.
    reply(res, {
      role: "assistant",
      content: "Done. Generated src/generated-summary.md from real tool outputs (search, image, web, todos, process)."
    });
  });
});

server.listen(PORT, "127.0.0.1", () => {
  // eslint-disable-next-line no-console
  console.log(`[golden-toolset-llm-server] listening on http://127.0.0.1:${PORT}`);
});
